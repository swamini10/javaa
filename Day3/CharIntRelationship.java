
// 9. Write a program to demonstrate char and int relationship.
public class CharIntRelationship {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'a';

        int value1 = ch1;  // Unicode value of 'A'
        int value2 = ch2;  // Unicode value of 'a'

        System.out.println("Character ch1 : " + ch1 + " -> int value: " + value1);
        System.out.println("Character ch2 : " + ch2 + " -> int value: " + value2);

    }
}
